"""Ablation matrix runner.

Reads the ``ablations`` list from ``configs/eval.yaml``, runs each named
ablation on the selected slice, and writes a single Markdown table to
``docs/ABLATIONS.md`` with 95% CIs.
"""

from __future__ import annotations

import asyncio
from collections.abc import Iterable
from pathlib import Path

from finqa_bot.config import AblationSpec, EvalConfig, GpuConfig, Settings
from finqa_bot.eval.harness import run_eval
from finqa_bot.logging import get_logger
from finqa_bot.types import EvalSummary

log = get_logger(__name__)


def run_ablation_matrix(
    gpu_cfg: GpuConfig,
    eval_cfg: EvalConfig,
    settings: Settings,
    slice_name: str = "dev_200",
    n: int = 200,
    out_path: Path | str = Path("docs/ABLATIONS.md"),
    ablations: Iterable[AblationSpec] | None = None,
) -> list[EvalSummary]:
    """Run every ablation + the default config, then write a Markdown table."""
    specs = list(ablations) if ablations else list(eval_cfg.ablations)
    results: list[EvalSummary] = []

    log.info("Running default (no-ablation) baseline on slice %s (n=%d) ...", slice_name, n)
    default = asyncio.run(
        run_eval(
            gpu_cfg=gpu_cfg,
            eval_cfg=eval_cfg,
            settings=settings,
            slice_name=slice_name,
            n=n,
            ablation_id=None,
            out_path=Path("runs") / "default.json",
        )
    )
    default.ablation_id = "default"
    results.append(default)

    for spec in specs:
        log.info("Ablation %s ...", spec.id)
        try:
            summary = asyncio.run(
                run_eval(
                    gpu_cfg=gpu_cfg,
                    eval_cfg=eval_cfg,
                    settings=settings,
                    slice_name=slice_name,
                    n=n,
                    ablation_id=spec.id,
                    out_path=Path("runs") / f"ablation_{spec.id}.json",
                )
            )
            results.append(summary)
        except Exception as exc:
            log.exception("Ablation %s failed: %s", spec.id, exc)
            continue

    _write_markdown_table(out_path, results, slice_name=slice_name, n=n)
    return results


def _write_markdown_table(
    out_path: Path | str,
    results: list[EvalSummary],
    slice_name: str,
    n: int,
) -> None:
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    header = (
        "| Ablation | ExeAcc | 95% CI | ProgAcc | Recall@5 | Grounded | Refused | p50 (ms) | p95 (ms) | $/correct |\n"
        "|---|---|---|---|---|---|---|---|---|---|\n"
    )
    body_lines: list[str] = []
    for r in results:
        row = (
            f"| {r.ablation_id or 'default'} "
            f"| {r.execution_accuracy:.3f} "
            f"| [{r.ci_low:.3f}, {r.ci_high:.3f}] "
            f"| {r.program_accuracy:.3f} "
            f"| {r.retrieval_recall.get(5, 0.0):.3f} "
            f"| {r.groundedness_rate:.3f} "
            f"| {r.refusal_rate:.3f} "
            f"| {r.latency_ms.get('p50', 0.0):.0f} "
            f"| {r.latency_ms.get('p95', 0.0):.0f} "
            f"| ${r.cost_per_correct_usd:.4f} |"
        )
        body_lines.append(row)

    content = (
        "# FinQA ablation matrix\n\n"
        f"Slice: **{slice_name}** (n={n}). Generated by `finqa-bot ablate`.\n\n"
        + header
        + "\n".join(body_lines)
        + "\n\nEach ablation toggles one component of the production pipeline. The\n"
        "`default` row uses the full stack. A negative delta (e.g. lower execution\n"
        "accuracy) on an ablation proves that component is contributing.\n"
    )
    out_path.write_text(content, encoding="utf-8")
    log.info("Wrote ablation table to %s", out_path)
